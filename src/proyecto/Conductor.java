    public boolean validarCajas() {
        
        if (jTFIdent.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Ingresar Identificación");
            return false;
        } else if (jTFNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Ingresar Nombre");
            return false;
        } else if (jTFApellido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Ingresar Apellido");
            return false;
        } else if (jTFCelular.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Ingresar celular");
            return false;
        } else if (jTFCorreo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Ingresar correo");
            return false;
        }else if (jTFDireccion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Ingresar Dirección");
            return false;
        }else if (jTFRestLicenCond.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Ingresar Restriccion de Licencia");
            return false;
        }else if (jCBCatLicen.getSelectedItem().equals("Seleccionar Categoria:")) {
            JOptionPane.showMessageDialog(null,
                    "Seleccionar Marca");
            return false;
        }else if (jCBCiudadLicen.getSelectedItem().equals("Seleccionar Ciudad:")) {
            JOptionPane.showMessageDialog(null,
                    "Seleccionar Marca");
            return false;
        }  else {
            return true;
        }
    }
